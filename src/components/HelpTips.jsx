export default function HelpTips({ section }) {
	const [isOpen, setIsOpen] = useState(false);
	const [expandedSections, setExpandedSections] = useState({});

	const help = SECTION_HELP[section];
	if (!help) return null;

	const toggleSection = (idx) => {
		setExpandedSections((prev) => ({
			...prev,
			[idx]: !prev[idx],
		}));
	};

	return (
		<>
			<button
				className="help-toggle-btn"
				onClick={() => setIsOpen(true)}
				title="Подсказки по разделу"
			>
				<HelpCircle size={18} />
				<span>Подсказки</span>
			</button>

			{isOpen && (
				<div className="help-overlay" onClick={() => setIsOpen(false)}>
					<div className="help-modal" onClick={(e) => e.stopPropagation()}>
						<div className="help-header">
							<h3>{help.title}</h3>
							<button className="btn btn-icon" onClick={() => setIsOpen(false)}>
								<X size={20} />
							</button>
						</div>
						<div className="help-body">
							<p className="help-description">{help.description}</p>
							{help.sections?.map((section, idx) => (
								<div key={idx} className="help-section">
									<button
										className="help-section-header"
										onClick={() => toggleSection(idx)}
									>
										<span>{section.name}</span>
										{expandedSections[idx] ? (
											<ChevronUp size={16} />
										) : (
											<ChevronDown size={16} />
										)}
									</button>
									{expandedSections[idx] && (
										<div className="help-section-content">
											<ul>
												{section.items?.map((item, i) => (
													<li key={i}>{item}</li>
												))}
											</ul>
										</div>
									)}
								</div>
							))}
						</div>
					</div>
				</div>
			)}
		</>
	);
}
